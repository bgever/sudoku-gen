import { Board } from '../../types/board.type';
import { rotateBoard90 } from './rotate-board-90.util';

const board: Board = [
  ['1', '2', '-', '4', '5', '-', '7', '8', '-'],
  ['-', '-', '6', '7', '8', '9', '1', '-', '3'],
  ['7', '-', '9', '-', '2', '3', '-', '5', '6'],
  ['2', '3', '4', '5', '-', '7', '-', '-', '1'],
  ['5', '6', '7', '-', '-', '-', '2', '3', '4'],
  ['-', '9', '-', '2', '3', '4', '5', '6', '-'],
  ['3', '-', '5', '-', '-', '8', '9', '1', '2'],
  ['-', '7', '8', '9', '1', '-', '-', '4', '5'],
  ['9', '1', '-', '3', '4', '5', '6', '-', '-'],
];

describe('rotateBoard90', () => {
  test('returns the Board rotated 90 degrees', () => {
    const expected: Board = [
      ['9', '-', '3', '-', '5', '2', '7', '-', '1'],
      ['1', '7', '-', '9', '6', '3', '-', '-', '2'],
      ['-', '8', '5', '-', '7', '4', '9', '6', '-'],
      ['3', '9', '-', '2', '-', '5', '-', '7', '4'],
      ['4', '1', '-', '3', '-', '-', '2', '8', '5'],
      ['5', '-', '8', '4', '-', '7', '3', '9', '-'],
      ['6', '-', '9', '5', '2', '-', '-', '1', '7'],
      ['-', '4', '1', '6', '3', '-', '5', '-', '8'],
      ['-', '5', '2', '-', '4', '1', '6', '3', '-'],
    ];

    expect(rotateBoard90(board)).toEqual(expected);
  });
});
